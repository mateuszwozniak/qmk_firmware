#include QMK_KEYBOARD_H

uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case RALT_T(KC_EQL):
            return 110;
        default:
            return TAPPING_TERM;
    }
}


